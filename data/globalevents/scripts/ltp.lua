function onThink(interval, lastExecution, thinkInterval) 
for _, v in pairs(config) do
	local player, rangeX, rangeY = 0, math.abs((v.leftTopCorner.x - v.rightBottomCorner.x) / 2), math.abs((v.leftTopCorner.y - v.rightBottomCorner.y) / 2)
	for z = math.min(v.leftTopCorner.z, v.rightBottomCorner.z), math.max(v.leftTopCorner.z, v.rightBottomCorner.z) do
		if getSpectators({x=v.leftTopCorner.x+rangeX,y=v.leftTopCorner.y+rangeY,z=z}, rangeX, rangeY) then
			for _, cid in ipairs(getSpectators({x=v.leftTopCorner.x+rangeX,y=v.leftTopCorner.y+rangeY,z=z}, rangeX, rangeY)) do
				player = player + (isPlayer(cid) and 1 or 0)
			end
		end
	end
	doSendAnimatedText(v.teleportPosition, player .. ' players', v.textColor)
end
return true
end